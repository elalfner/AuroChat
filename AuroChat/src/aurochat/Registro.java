    private void btn_regisbtn_send2btn_sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_regisbtn_send2btn_sendActionPerformed
        Socket socket;

        try {

            socket = new Socket(Login.ip, 2223);

            String nombre = txt_nombre.getText();
            String apellido = txt_apellido.getText();
            String username = txt_adduser.getText();
            String username2 = txt_adduser2.getText();
            String password = txt_addpassw.getText();
            String password2 = txt_addpassw2.getText();

            String status = "";

            if (!"".equals(nombre) && !"".equals(apellido) && !"".equals(username) && !"".equals(username2) && !"".equals(password) && !"".equals(password2)) {
                if (username.equals(username2)) {

                    if (password.equals(password2)) {

                        if ("0".equals(enc)) {
                            JSONObject json = new JSONObject();
                            json.put("fullname", nombre + " " + apellido);
                            json.put("username", username);
                            json.put("password", password);

                            ObjectOutputStream out = new ObjectOutputStream(socket.getOutputStream());
                            out.writeObject(json.toString());

                            InputStream inn = socket.getInputStream();
                            ObjectInputStream in = new ObjectInputStream(inn);
                            
                            String jobj = (String) in.readObject();
                            json = new JSONObject(jobj);
                            
                            status = json.getString("status");

                            System.out.println(status);
                            if ("OK".equals(status)) {
                                JOptionPane.showMessageDialog(null, "Se ha agregado correctamente");
                                enc = "";
                                lbl_ok.setVisible(false);
                            } else {
                                JOptionPane.showMessageDialog(null, status);
                            }

                        } else {
                            JOptionPane.showMessageDialog(null, "Este usuario ya existe");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "La contrase単a y la contrase単a de confirmacion deben de ser el mismo");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "El nombre de usuario y el nombre de usuario de confirmacion deben de ser el mismo");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Llena todos los campos");
            }

        } catch (Exception ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_btn_regisbtn_send2btn_sendActionPerformed

